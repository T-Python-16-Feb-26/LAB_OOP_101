class Panda:
     def __init__(self, name:str, age, color, favorite_food):
          self.name = name 
          self.age = age
          self.color = color 
          self.favorite_food = favorite_food

    #methods
     def eat(self):
          print(f"{self.name} is eating {self.favorite_food} ")
               
     def sleep(self):
          print(f"{self.name} is sleeping")

          
